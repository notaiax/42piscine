#include <unistd.h>

int	main(int argc, char **argv)
{
	int i = 0;
	char n = '\n';
	char a = 'a';

	if (argc - 1 != 1)
	{	
		write(1, &a, 1);
		write(1, &n, 1);
		return (0);
	}
	while (argv[1][i] != 0)
	{
		if(argv[1][i] == 'a')
		{
			write(1, &argv[1][i], 1);
			break;
		}
		i++;
	}
	write(1, &n, 1);
	return (0);
}
