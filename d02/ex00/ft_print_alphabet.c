int		ft_putchar(char c);

void	ft_print_alphabet(void)
{
	int		i;
	char	a;
	char	z;

	a = 'a';
	z = 'z';
	i = a;
	while (i <= z)
	{
		ft_putchar(i);
		i = i + 1;
	}
}
